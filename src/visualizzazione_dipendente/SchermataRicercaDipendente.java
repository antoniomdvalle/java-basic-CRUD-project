    private void cercaIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cercaIdActionPerformed
        DipendenteDAO ddao = new DipendenteDAO();
        String idS = idTr.getText().strip();
        int idCorretto = Integer.parseInt(idS);
        Dipendente d = ddao.getDipendente(idCorretto);

        if(d == null){
            JOptionPane.showMessageDialog(null, "Dipendente non trovato.");
            idTr.setText("");
        }else{
            try {
                idD.setText(String.valueOf(d.getId()));
                nome.setText(d.getNome());
                data.setText(d.getAmmissione().toString());
                impresaD.setSelectedItem(d.getImpresa());
            }catch(Exception e){
                System.out.println("Errore trovato: "+ e.getMessage());
            }
            
            idTr.setText("");
        }

    }//GEN-LAST:event_cercaIdActionPerformed

