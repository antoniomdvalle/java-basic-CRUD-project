    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ImpresaDAO idao = new ImpresaDAO();
        Impresa i = new Impresa();
        try{
            String nomeAlt = nomeTr.getText().strip();

            i = idao.getImpresaNome(nomeAlt);
            if(i==null){
                JOptionPane.showMessageDialog(null,"Impresa non trovata.");
            }else{
                int idNuovo = i.getId();
                String nomeNuovo = i.getNomedellimpresa();
                String areaNuova = i.getAreadicompetenza().toString();
                String idTxt = Integer.toString(idNuovo);

                
                idTr.setText(idTxt);
                nome.setText(nomeNuovo);
                data.setText(areaNuova);
                impresaD.setSelectedItem(i);
                
            }

        }catch(Exception e){

        }
    }//GEN-LAST:event_jButton1ActionPerformed

