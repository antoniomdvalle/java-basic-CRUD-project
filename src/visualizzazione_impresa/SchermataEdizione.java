    private void aggiornaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aggiornaActionPerformed
        ImpresaDAO idao = new ImpresaDAO();
        Impresa i = new Impresa();
        try{
            int idNuovo = Integer.parseInt(idEs.getText());
            String nomeNuovo = nomeEs.getText();
            String areaNuova = areaEs.getSelectedItem().toString();

            i.setId(idNuovo);
            i.setNomedellimpresa(nomeNuovo);
            i.setAreadicompetenza(areaNuova);

            idao.editare(i);
            JOptionPane.showMessageDialog(null, "Dati inseriti correttamente.");
            
            ricierca.setEnabled(true);
            id.setEnabled(true);
            id.setText("");
            
            aggiorna.setEnabled(false);
            idEs.setEnabled(false);
            idEs.setText("");
            nomeEs.setEnabled(false);
            nomeEs.setText("");
            areaEs.setEnabled(false);
            
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_aggiornaActionPerformed

    private void riciercaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_riciercaActionPerformed
        ImpresaDAO idao = new ImpresaDAO();
        
        String idInt2 = id.getText().strip();
        int idInt = Integer.parseInt(idInt2);
        
        
        try{
            Impresa iCerta = idao.getImpresa(idInt);
            
            if(iCerta == null){
                JOptionPane.showMessageDialog(null, "Impresa non trovata.");
            }else{
            ricierca.setEnabled(false);
            id.setEnabled(false);
            
            aggiorna.setEnabled(true);
            idEs.setEnabled(true);
            nomeEs.setEnabled(true);
            areaEs.setEnabled(true);
            
            String idNuovo = Integer.toString(idInt);
            String nomeNuovo = iCerta.getNomedellimpresa();
            String areaNuova = iCerta.getAreadicompetenza();
            
            idEs.setText(idNuovo);
            nomeEs.setText(nomeNuovo);
            areaEs.setSelectedItem(areaNuova);
            }
        }catch(Exception e){
            System.out.println("Errore trovato: "+e.getMessage());
        }
    }//GEN-LAST:event_riciercaActionPerformed

