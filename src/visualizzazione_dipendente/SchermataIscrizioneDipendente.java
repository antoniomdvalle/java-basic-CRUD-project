    private void inserireActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserireActionPerformed
        Dipendente dipendente = new Dipendente();
        DipendenteDAO ddao = new DipendenteDAO();
        
        
        String nome = nomeD.getText().strip();
        Impresa impresaid = (Impresa) impresaD.getSelectedItem();
        
        dipendente.setNome(nome);
        dipendente.setImpresa(impresaid);
        
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        
        try{

            dipendente.setAmmissione(sdf.parse(data.getText()));

        }catch(Exception e){
            System.out.println("Errore trovato: " + e.getMessage());
        }
        
        ddao.inserire(dipendente);
        JOptionPane.showMessageDialog(null, "Dati inseriti con successo.");
        nomeD.setText("");
    }//GEN-LAST:event_inserireActionPerformed

